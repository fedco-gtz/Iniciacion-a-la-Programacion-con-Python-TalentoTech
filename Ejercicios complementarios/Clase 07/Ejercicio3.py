# |-------------------------------------------|
# | Ejercicio 3 - Calcular promedio de edades |
# |                                           |
# | Dada una lista de edades, escribe un      |
# | programa que calcule el promedio de       |
# | todas las edades.                         |
# | Lista de edades                           |
# | edades = [25, 30, 22, 28, 35, 40]         |
# |-------------------------------------------|

edades = [25, 30, 22, 28, 35, 40]

promedio = sum(edades) / len(edades)

print("El promedio de las edades es:", promedio)
