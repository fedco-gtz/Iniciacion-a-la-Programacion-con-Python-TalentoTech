# Solicito los numeros al usurario
num1 = int(input("Por favor, ingresa el primer número entero: "))
num2 = int(input("Por favor, ingresa el segundo número entero: "))

# Realización de las operaciones solicitadas
suma = num1 + num2
resta = num1 - num2
multiplicacion = num1 * num2
modulo = num1 % num2

# Mostrar los resultados
print(f"La suma de tus números es: {suma}")
print(f"La resta de tus números es: {resta}")
print(f"La multiplicación de tus números es: {multiplicacion}")
print(f"El módulo de tus números es: {modulo}")
